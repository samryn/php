<?php
$a=3;
$b=2;

echo $a."<br/>";       //a - 3 akan tampil 3
$a += 1;               //a berisi 3 + 1
echo $a."<br/>";      
$a -= $b;              //a berisi 4 - 2
echo $a."<br/>";       
$a *= $b;              //a berisi 2 x 2
echo $a."<br/>";       
$a /= $b;              //a berisi 4 / 2
echo $a."<br/>";       
$a .= $b;              //a berisi 2.2
echo $a."<br/>";       
$a %= $b;              //a berisi 22 % 2
echo $a."<br/>";       
?>